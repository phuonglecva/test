function interpolate(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (output, [key, value]) => output.replaceAll(`{{${key}}}`, String(value)),
    template
  );
}

export function translate(language: AppLanguage, key: keyof typeof translations.en, params?: TranslationParams) {
  const template = translations[language][key] ?? translations.vi[key] ?? key;
  return interpolate(template, params);
}

export function useI18n() {
  const language = useAppStore((state) => state.language);

  return {
    language,
    t: (key: keyof typeof translations.en, params?: TranslationParams) => translate(language, key, params)
  };
}
